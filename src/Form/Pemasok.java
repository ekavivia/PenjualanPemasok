    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(!(tfKode.getText().equals(""))&&
            !(tfNama.getText().equals(""))&&
            !(tfAlamat.getText().equals(""))&&
            !(tfTelepon.getText().equals(""))){            
            try{             
                String sql = "insert into pemasok values(?,?,?,?)";
                prestat = new fungsi.koneksi().konek.prepareStatement(sql);
                prestat.setString(1,tfKode.getText());
                prestat.setString(2,tfNama.getText());
                prestat.setString(3,tfAlamat.getText());
                prestat.setString(4,tfTelepon.getText());
                prestat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data telah disimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                hapusPadaForm();
                kodeOtomatis();
                tampilDatakeTabel(null);
            }catch(Exception e){
                System.out.println(""+e);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Username dan Password harus diisi");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

