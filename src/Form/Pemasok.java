    public void tampilDatakeTabel(String cari){
        try{
            String sql;
            if(!(cari==null)){
                sql = "select * from pemasok where kode_pemasok like '%" + cari + "%'";
            }else{
                sql = "select * from pemasok";
            }
            stat = new fungsi.koneksi().konek.createStatement();
            res = stat.executeQuery(sql);
            while(res.next()){
                String kode = res.getString("kode_pemasok");
                String nama = res.getString("nama");
                String alamat = res.getString("alamat");
                String telepon = res.getString("telepon");
                String data[] = {kode,nama,alamat,telepon};
                tabModel.addRow(data);
            }
        }catch(Exception e){
            System.out.println(""+e);
        }
    }
    public void hapusPadaForm(){
        int row = tabModel.getRowCount();
        for (int a=0; a<row; a++){
            tabModel.removeRow(0);
        }
    }
    public void kodeOtomatis(){
        try{
            String sql = "select * from pemasok "
                    + "order by kode_pemasok desc";
            stat = new fungsi.koneksi().konek.createStatement();
            res = stat.executeQuery(sql);
            if(res.next()){
                String kode = res.getString("kode_pemasok").substring(1);
                String angka = ""+(Integer.parseInt(kode)+1);
                String nol = "";
                if(angka.length()==1){
                    nol = "00";
                }else if(angka.length()==2){
                    nol = "0";
                }if(angka.length()==3){
                    nol = "";
                }
                tfKode.setText("P" + nol + angka);
            }else{
                tfKode.setText("P001");
            }
        }catch(Exception e){
            System.out.println(""+e);
        }
    }


