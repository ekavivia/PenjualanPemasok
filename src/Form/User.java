    public void tampilDatakeTabel(String cariUser){
        try{
            String sql;
            if(!(cariUser==null)){
                sql = "select * from user where username like '%" + cariUser + "%'";
            }else{
                sql = "select * from user";
            }
            stat = new fungsi.koneksi().konek.createStatement();
            res = stat.executeQuery(sql);
            while(res.next()){
                String username = res.getString("username");
                String password = res.getString("password");
                String status = res.getString("status");
                String data[] = {username,password,status};
                tabModel.addRow(data);
            }
        }catch(Exception e){
            System.out.println(""+e);
        }
    }
    public void hapusPadaForm(){
        int row = tabModel.getRowCount();
        for (int a=0; a<row; a++){
            tabModel.removeRow(0);
        }
    }

