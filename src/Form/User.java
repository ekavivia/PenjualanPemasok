    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(!(tfUsername.getText().equals(""))&&
                !(pfPassword.getText().equals(""))){
        try{
            String sql = "insert into user (username, "
                    + "password) values (?,md5(?))";
            prestat = new fungsi.koneksi().konek.prepareStatement(sql);
            prestat.setString(1,tfUsername.getText());
            prestat.setString(2,pfPassword.getText());
            prestat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data telah disimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            hapusPadaForm();
            tampilDatakeTabel(null);
        }catch(Exception e){
            System.out.println(""+e);
        }
      }else{
        JOptionPane.showMessageDialog(rootPane, "Username dan Password harus diisi");
      }
    }//GEN-LAST:event_btnSimpanActionPerformed

