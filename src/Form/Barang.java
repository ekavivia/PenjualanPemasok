    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(!(tfKodeBarang.getText().equals(""))&&
            !(cmbPemasok.getSelectedItem().equals(""))&&    
            !(tfNama.getText().equals(""))&&
            !(tfHargaBeli.getText().equals(""))&&
            !(tfHargaJual.getText().equals(""))&&    
            !(tfStok.getText().equals(""))){
            try{
                String sql = "insert into barang values(?,?,?,?,?,?)";
                prestat = new fungsi.koneksi().konek.prepareStatement(sql);
                prestat.setString(1,tfKodeBarang.getText());
                String pilihpemasok = cmbPemasok.getSelectedItem().toString();
                String kodepemasok = pilihpemasok.substring(0,5);
                prestat.setString(2,kodepemasok);
                prestat.setString(3,tfNama.getText());
                prestat.setInt(4,Integer.parseInt(tfHargaBeli.getText()));
                prestat.setInt(5,Integer.parseInt(tfHargaJual.getText()));
                prestat.setInt(6,Integer.parseInt(tfStok.getText()));
                prestat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data telah disimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                hapusPadaForm();
                tampilDatakeTabel(null);
            }catch(Exception e){
                System.out.println(""+e);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Username dan Password harus diisi");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

