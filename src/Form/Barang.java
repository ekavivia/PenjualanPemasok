    public void isiPemasok(){
        cmbPemasok.removeAllItems();
        cmbPemasok.addItem("Pilih Pemasok");
        try{
            String sql = "select * from pemasok";
            stat = new fungsi.koneksi().konek.createStatement();
            res = stat.executeQuery(sql);
            while(res.next()){
                String kode = res.getString("kode_pemasok");
                String nama = res.getString("nama");
                cmbPemasok.addItem(kode+" : "+nama);
            }
        }catch(Exception e){
            System.out.println(""+e);
        }
    }
    public void tampilDatakeTabel(String cari){
        try{
            String sql;
            if(!(cari==null)){
                sql = "select * from barang where kode_barang like '%" + cari + "%'";
            }else{
                sql = "select * from barang";
            }
            stat = new fungsi.koneksi().konek.createStatement();
            res = stat.executeQuery(sql);
            while(res.next()){
                String kodebarang = res.getString("kode_barang");
                String kodepemasok = res.getString("kode_pemasok");
                String nama = res.getString("nama");
                String hargabeli = res.getString("hargabeli");
                String hargajual = res.getString("hargajual");
                String stok = res.getString("stok");
                String data[] = {kodebarang, kodepemasok, nama, hargabeli, hargajual, stok};
                tabModel.addRow(data);
            }
        }catch(Exception e){
            System.out.println(""+e);
        }
    }
    
    public void kodeOtomatis(){
        try{
            String sql = "select * from barang "
                    + "order by kode_barang desc";
            stat = new fungsi.koneksi().konek.createStatement();
            res = stat.executeQuery(sql);
            if(res.next()){
                String kode = res.getString("kode_barang").substring(1);
                String angka = ""+(Integer.parseInt(kode)+1);
                String nol = "";
                if(angka.length()==1){
                    nol = "00";
                }else if(angka.length()==2){
                    nol = "0";
                }if(angka.length()==3){
                    nol = "";
                }
                tfKodeBarang.setText("B" + nol + angka);
            }else{
                tfKodeBarang.setText("B001");
            }
        }catch(Exception e){
            System.out.println(""+e);
        }
    }
    
    public void hapusPadaForm(){
        int row = tabModel.getRowCount();
        for (int a=0; a<row; a++){
            tabModel.removeRow(0);
        }
    }

